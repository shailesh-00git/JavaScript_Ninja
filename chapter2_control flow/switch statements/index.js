// switch statements
let grade = "B";
switch (grade) {
  case "A":
    console.log("You got A grade");
    break;
  case "B":
    console.log("You got B grade");
    break;
  case "C":
    console.log("You got C grade");
    break;
  case "D":
    console.log("You got D grade");
    break;
  case "E":
    console.log("You got E grade");
    break;
  default:
    console.log("Enter valid Grade");
}
